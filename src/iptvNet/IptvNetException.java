package iptvNet;

public class IptvNetException extends Exception {

	private Throwable cause;

	public IptvNetException(String message) {
		super(message);
	}

	public IptvNetException(Throwable t) {
		super(t.getMessage());
		this.cause = t;
	}

	public Throwable getCause() {
		return this.cause;
	}

}
